def sumar(n1, n2):
	print("La suma es: " + str(n1 + n2))

def restar(n1, n2):
	print("La resta es: " + str(n1 - n2))

def multiplicar(n1, n2):
	print("La multiplicacion es: " + str(n1 * n2))

def dividir(n1, n2):
	print("La division es: " + str(n1 / n2))

def potenciar(base, exponente):
	print("La potencia es: " + str(base ** exponente))

def redondear(n1):
	print("El redondeo es: " + str(round(n1)))


